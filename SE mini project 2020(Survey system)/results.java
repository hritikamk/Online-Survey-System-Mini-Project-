    public void result(int qid)
    {
        Statement stmt=null;
        this.setVisible(true);
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/survey", "root", "kaziatiq201");
            stmt = con.createStatement();
            Statement st=con.createStatement();
            String sql = "SELECT option1, option2, option3, option4 FROM ans WHERE A_ID="+qid;
            String sql2 = "SELECT survey_question FROM surveys WHERE Q_ID="+qid;
            ResultSet rs = stmt.executeQuery(sql);
            ResultSet r=st.executeQuery(sql2);
            while(rs.next())
            {
                int o1=rs.getInt("option1");
                int o2=rs.getInt("option2");
                int o3=rs.getInt("option3");
                int o4=rs.getInt("option4");
                int sum=o1+o2+o3+o4;
                float op1=(o1*100/sum);
                float op2=(o2*100/sum);
                float op3=(o3*100/sum);
                float op4=(o4*100/sum);
                jLabel2.setText("Option 1:"+op1+"%");
                jLabel3.setText("Option 2:"+op2+"%");
                jLabel4.setText("Option 3:"+op3+"%");
                jLabel5.setText("Option 4:"+op4+"%");
            }
            if (r.next())
            {
                String s=r.getString("survey_question");
                jLabel1.setText(s);
            }

    }catch(Exception e){System.out.println(e);}
    }

